package model;

public class ClotheVO {

	private int cl_code;
	private String cl_top;
	private String cl_bottom;
	private String cl_content;
	
	public int getCl_code() {
		return cl_code;
	}
	public void setCl_code(int cl_code) {
		this.cl_code = cl_code;
	}
	public String getCl_top() {
		return cl_top;
	}
	public void setCl_top(String cl_top) {
		this.cl_top = cl_top;
	}
	public String getCl_bottom() {
		return cl_bottom;
	}
	public void setCl_bottom(String cl_bottom) {
		this.cl_bottom = cl_bottom;
	}
	public String getCl_content() {
		return cl_content;
	}
	public void setCl_content(String cl_content) {
		this.cl_content = cl_content;
	}
	
	
}
