package model;

public class ColorVO {

	private int color_code;
	private String color_kind;
	private String color_content;
	
	public int getColor_code() {
		return color_code;
	}
	public void setColor_code(int color_code) {
		this.color_code = color_code;
	}
	public String getColor_kind() {
		return color_kind;
	}
	public void setColor_kind(String color_kind) {
		this.color_kind = color_kind;
	}
	public String getColor_content() {
		return color_content;
	}
	public void setColor_content(String color_content) {
		this.color_content = color_content;
	}
	
	
}
