package model;

public class HairVO {

	private int hair_code;
	private String hair_style;
	private String hair_content;
	
	public int getHair_code() {
		return hair_code;
	}
	public void setHair_code(int hair_code) {
		this.hair_code = hair_code;
	}
	public String getHair_style() {
		return hair_style;
	}
	public void setHair_style(String hair_style) {
		this.hair_style = hair_style;
	}
	public String getHair_content() {
		return hair_content;
	}
	public void setHair_content(String hair_content) {
		this.hair_content = hair_content;
	}

}
