package model;

public class MoodVO {

	private int mood_code;
	private String mood_kind;
	private String mood_content;
	
	public int getMood_code() {
		return mood_code;
	}
	public void setMood_code(int mood_code) {
		this.mood_code = mood_code;
	}
	public String getMood_kind() {
		return mood_kind;
	}
	public void setMood_kind(String mood_kind) {
		this.mood_kind = mood_kind;
	}
	public String getMood_content() {
		return mood_content;
	}
	public void setMood_content(String mood_content) {
		this.mood_content = mood_content;
	}
	
}
