package model;

public class PerfumeVO {

	private int perfume_code;
	private String perfume_type;
	private String perfume_content;
	
	public int getPerfume_code() {
		return perfume_code;
	}
	public void setPerfume_code(int perfume_code) {
		this.perfume_code = perfume_code;
	}
	public String getPerfume_type() {
		return perfume_type;
	}
	public void setPerfume_type(String perfume_type) {
		this.perfume_type = perfume_type;
	}
	public String getPerfume_content() {
		return perfume_content;
	}
	public void setPerfume_content(String perfume_content) {
		this.perfume_content = perfume_content;
	}
	
	
}
