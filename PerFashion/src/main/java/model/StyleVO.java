package model;

public class StyleVO {

	private int style_code;
	private String style_kind;
	private String style_content;
	
	public int getStyle_code() {
		return style_code;
	}
	public void setStyle_code(int style_code) {
		this.style_code = style_code;
	}
	public String getStyle_kind() {
		return style_kind;
	}
	public void setStyle_kind(String style_kind) {
		this.style_kind = style_kind;
	}
	public String getStyle_content() {
		return style_content;
	}
	public void setStyle_content(String style_content) {
		this.style_content = style_content;
	}
	
	
}
